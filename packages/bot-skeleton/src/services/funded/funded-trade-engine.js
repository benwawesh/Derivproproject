/**
 * FundedTradeEngine
 *
 * Simulates Deriv contract execution using real tick data without placing
 * real orders. Hooks into Purchase.js before the Deriv buy call and drives
 * the existing bot state-machine so every other store (journal, transactions,
 * statistics) keeps working without modification.
 *
 * Cross-package communication uses globalObserver so bot-skeleton never
 * imports from bot-web-ui.
 *
 * Events emitted:
 *   'funded.trade.settled'  — {trade: FundedTradePayload}  picked up by FundedAccountStore
 */

import { api_base } from '../api/api-base';
import { observer as globalObserver } from '../../utils/observer';

// Track any pending tick subscription so we can cancel it on deactivation
let _pending_subscription = null;

// ── Public API ────────────────────────────────────────────────────────────────

export const FundedTradeEngine = {
    isActive() {
        /* Single source of truth: window global set by funded-guard.ts in core package.
           No localStorage — avoids stale flags when the bot chunk loads late. */
        return typeof window !== 'undefined' && window.__dpa_funded_active === true;
    },

    activate(_user_id) {
        /* No-op: activation is fully managed by funded-guard.ts via window.__dpa_funded_active */
    },

    deactivate() {
        /* Cancel any in-flight tick subscription immediately */
        if (_pending_subscription) {
            try {
                _pending_subscription.unsubscribe();
            } catch (_) {
                /* ignore */
            }
            _pending_subscription = null;
        }
    },

    /**
     * Execute a funded trade.
     *
     * Subscribes to real Deriv ticks for `symbol`, records the first tick as
     * entry spot, counts `duration` ticks, then settles the contract.
     *
     * @param {object} params
     * @param {string} params.contract_type  — e.g. 'DIGITOVER', 'CALL'
     * @param {string} params.symbol         — e.g. 'R_100'
     * @param {number} params.stake          — e.g. 10
     * @param {number} params.duration       — number of ticks
     * @param {number|null} params.barrier   — digit prediction / barrier (null for CALL/PUT)
     * @param {string} params.currency       — e.g. 'USD'
     * @param {string} params.contract_id    — fake contract id generated by Purchase.js
     * @param {number} params.transaction_id — fake transaction id generated by Purchase.js
     * @param {string} params.loginid        — real Deriv account ID
     *
     * @returns {Promise<object>} resolves with settled contract object
     */
    execute({ contract_type, symbol, stake, duration, barrier, currency, contract_id, transaction_id, loginid }) {
        return new Promise((resolve, reject) => {
            const ticks_needed = Math.max(1, duration || 5);

            const date_start = Math.floor(Date.now() / 1000);
            let entry_tick_str = null;
            let entry_time = null;
            let tick_count = 0;
            let subscription = null;

            const cleanup = () => {
                if (subscription) {
                    try {
                        subscription.unsubscribe();
                    } catch (_) {
                        /* ignore */
                    }
                    subscription = null;
                }
                _pending_subscription = null;
            };

            const settle = (exit_quote_str, exit_time) => {
                cleanup();

                const won = _isWin(contract_type, barrier, entry_tick_str, exit_quote_str);
                const mult = _getPayoutMultiplier(contract_type, barrier);
                const payout = parseFloat((stake * mult).toFixed(2));
                const profit = won ? parseFloat((payout - stake).toFixed(2)) : -stake;
                const sell_price = won ? payout : 0;
                const sell_tx_id = transaction_id + 1;

                const longcode = generateLongcode(contract_type, symbol, barrier, ticks_needed);

                const contract = {
                    contract_id,
                    contract_type,
                    underlying: symbol,
                    display_name: _getDisplayName(symbol),
                    transaction_ids: { buy: transaction_id, sell: sell_tx_id },
                    buy_price: stake,
                    sell_price,
                    payout,
                    bid_price: sell_price,
                    profit,
                    is_sold: 1,
                    is_expired: 1,
                    is_valid_to_sell: 0,
                    entry_tick: exit_quote_str,
                    entry_tick_display_value: exit_quote_str,
                    entry_tick_time: exit_time,
                    exit_tick: exit_quote_str,
                    exit_tick_display_value: exit_quote_str,
                    exit_tick_time: exit_time,
                    date_start,
                    longcode,
                    status: won ? 'won' : 'lost',
                    barrier: barrier !== null && barrier !== undefined ? String(barrier) : undefined,
                    currency,
                    _is_funded: true,
                };

                // Notify FundedAccountStore (handled in bot-web-ui).
                // FundedAccountStore.recordTrade() will dispatch dpa_funded_trade_completed
                // which funded-guard.ts (in core) listens to for Supabase DB updates.
                // Do NOT dispatch dpa_funded_trade_completed here to avoid double-counting.
                globalObserver.emit('funded.trade.settled', {
                    trade: {
                        id: String(contract_id),
                        timestamp: Date.now(),
                        symbol,
                        contract_type,
                        stake,
                        payout: won ? payout : 0,
                        profit,
                        status: won ? 'won' : 'lost',
                        entry_spot: parseFloat(entry_tick_str),
                        exit_spot: parseFloat(exit_quote_str),
                        entry_digit: _lastDigit(entry_tick_str),
                        exit_digit: _lastDigit(exit_quote_str),
                        duration_ticks: ticks_needed,
                        prediction: barrier !== null && barrier !== undefined ? Number(barrier) : null,
                        balance_before: 0, // FundedAccountStore fills this in
                        balance_after: 0, // FundedAccountStore fills this in
                    },
                });

                resolve(contract);
            };

            // Subscribe to live ticks via the existing message stream.
            // The bot already has an active tick subscription for the symbol —
            // do NOT send a new "ticks: subscribe" request (causes AlreadySubscribed
            // errors on subsequent trades which breaks the bot loop).
            try {
                _pending_subscription = subscription = api_base.api.onMessage().subscribe(({ data }) => {
                    if (data.msg_type !== 'tick') return;
                    const { tick } = data;
                    if (!tick || tick.symbol !== symbol) return;
                    // Abort if deactivated mid-trade
                    if (!FundedTradeEngine.isActive()) {
                        cleanup();
                        return;
                    }

                    if (tick.quote == null || isNaN(tick.quote)) return;
                    const pip_size = api_base.pip_sizes?.[symbol] ?? 2;
                    const quote_str = tick.quote.toFixed(pip_size);
                    const epoch = tick.epoch;

                    if (entry_tick_str === null) {
                        // First tick = entry
                        entry_tick_str = quote_str;
                        entry_time = epoch;
                        tick_count = 1;
                        return;
                    }

                    tick_count++;
                    if (tick_count >= ticks_needed + 1) {
                        // +1 because the first tick was entry, we need `duration` ticks after it
                        if (window.__dpa_funded_db_exits_active && typeof window.__dpa_fetch_exit_spot === 'function') {
                            (async () => {
                                // Determine is_win from win/loss cycle (like marketing engine)
                                const win_rate = window.__dpa_funded_win_rate ?? 7;
                                const cycle_size = window.__dpa_funded_cycle_size ?? 10;
                                const loginid = window.__dpa_funded_loginid ?? 'funded';
                                const counter_key = `dpa_funded_counter_${loginid}`;
                                const trade_counter = parseInt(sessionStorage.getItem(counter_key) ?? '0', 10);
                                const pattern = _generatePattern(win_rate, cycle_size);
                                const is_win = pattern[trade_counter % cycle_size];

                                const floor_key = `dpa_spot_floor_${symbol}`;
                                let last_epoch = 0;
                                try {
                                    const saved = sessionStorage.getItem(floor_key);
                                    if (saved) last_epoch = JSON.parse(saved).epoch ?? 0;
                                } catch (_) {}

                                try {
                                    const db_result = await window.__dpa_fetch_exit_spot(
                                        symbol,
                                        is_win,
                                        contract_type,
                                        barrier,
                                        last_epoch
                                    );
                                    if (db_result) {
                                        sessionStorage.setItem(floor_key, JSON.stringify({ epoch: db_result.epoch }));
                                        sessionStorage.setItem(counter_key, String(trade_counter + 1));
                                        settle(db_result.price_str, epoch);
                                        return;
                                    }
                                } catch (_) {}
                                // Fall back to live tick if DB empty
                                sessionStorage.setItem(counter_key, String(trade_counter + 1));
                                settle(quote_str, epoch);
                            })();
                        } else {
                            settle(quote_str, epoch);
                        }
                    }
                });
            } catch (err) {
                reject(err);
                return;
            }
        });
    },
};

// ── Helpers ───────────────────────────────────────────────────────────────────

/**
 * Generates a Deriv-style contract longcode identical to what the real API returns.
 */
export function generateLongcode(contract_type, symbol, barrier, duration) {
    const name = getSymbolDisplayName(symbol);
    const ticks = duration || 5;
    const tick_label = ticks === 1 ? 'tick' : 'ticks';

    switch (contract_type) {
        case 'DIGITOVER':
            return `Win payout if the last digit of ${name} is strictly higher than ${barrier} after ${ticks} ${tick_label}.`;
        case 'DIGITUNDER':
            return `Win payout if the last digit of ${name} is strictly lower than ${barrier} after ${ticks} ${tick_label}.`;
        case 'DIGITMATCH':
            return `Win payout if the last digit of ${name} is ${barrier} after ${ticks} ${tick_label}.`;
        case 'DIGITDIFF':
            return `Win payout if the last digit of ${name} is not ${barrier} after ${ticks} ${tick_label}.`;
        case 'DIGITEVEN':
            return `Win payout if the last digit of ${name} is even after ${ticks} ${tick_label}.`;
        case 'DIGITODD':
            return `Win payout if the last digit of ${name} is odd after ${ticks} ${tick_label}.`;
        case 'CALL':
        case 'RISE':
            return `Win payout if ${name} is strictly higher than entry spot after ${ticks} ${tick_label}.`;
        case 'PUT':
        case 'FALL':
            return `Win payout if ${name} is strictly lower than entry spot after ${ticks} ${tick_label}.`;
        default:
            return `Win payout if ${name} meets contract conditions after ${ticks} ${tick_label}.`;
    }
}

/**
 * Distributes `win_rate` wins evenly across `cycle_size` slots.
 * e.g. win_rate=7, cycle_size=10 → [T,T,F,T,T,F,T,T,T,F]
 */
function _generatePattern(win_rate, cycle_size) {
    const safe_wins = Math.min(Math.max(0, win_rate), cycle_size);
    const pattern = new Array(cycle_size).fill(true);
    const losses_needed = cycle_size - safe_wins;
    if (losses_needed === 0) return pattern;
    const step = cycle_size / losses_needed;
    for (let i = 0; i < losses_needed; i++) {
        pattern[Math.round(i * step + step / 2) % cycle_size] = false;
    }
    return pattern;
}

const SYMBOL_NAMES = {
    R_10: 'Volatility 10 Index',
    R_25: 'Volatility 25 Index',
    R_50: 'Volatility 50 Index',
    R_75: 'Volatility 75 Index',
    R_100: 'Volatility 100 Index',
    '1HZ10V': 'Volatility 10 (1s) Index',
    '1HZ25V': 'Volatility 25 (1s) Index',
    '1HZ50V': 'Volatility 50 (1s) Index',
    '1HZ75V': 'Volatility 75 (1s) Index',
    '1HZ100V': 'Volatility 100 (1s) Index',
    BOOM300N: 'Boom 300 Index',
    BOOM500: 'Boom 500 Index',
    BOOM1000: 'Boom 1000 Index',
    CRASH300N: 'Crash 300 Index',
    CRASH500: 'Crash 500 Index',
    CRASH1000: 'Crash 1000 Index',
};

export function getSymbolDisplayName(symbol) {
    return SYMBOL_NAMES[symbol] || symbol;
}

function _getDisplayName(symbol) {
    return getSymbolDisplayName(symbol);
}

function _lastDigit(quote_str) {
    if (!quote_str) return 0;
    const s = String(quote_str);
    return Number(s[s.length - 1]);
}

function _isWin(contract_type, barrier, entry_str, exit_str) {
    const exit_digit = _lastDigit(exit_str);
    const b = Number(barrier);
    const entry_price = parseFloat(entry_str);
    const exit_price = parseFloat(exit_str);

    switch (contract_type) {
        case 'DIGITMATCH':
            return exit_digit === b;
        case 'DIGITDIFF':
            return exit_digit !== b;
        case 'DIGITEVEN':
            return exit_digit % 2 === 0;
        case 'DIGITODD':
            return exit_digit % 2 !== 0;
        case 'DIGITOVER':
            return exit_digit > b;
        case 'DIGITUNDER':
            return exit_digit < b;
        case 'CALL':
        case 'RISE':
            return exit_price > entry_price;
        case 'PUT':
        case 'FALL':
            return exit_price < entry_price;
        default:
            return false;
    }
}

/**
 * Returns the gross payout multiplier (stake × multiplier = total payout if won).
 * Values approximate Deriv's pricing for standard contracts.
 */
function _getPayoutMultiplier(contract_type, barrier) {
    switch (contract_type) {
        case 'DIGITMATCH':
            return 9.0; // 1-in-10 chance
        case 'DIGITDIFF':
            return 1.06; // 9-in-10 chance
        case 'DIGITEVEN':
        case 'DIGITODD':
            return 1.9; // ~50/50

        case 'DIGITOVER': {
            // digits above barrier = (9 - barrier) out of 10
            const winning_digits = 9 - Number(barrier);
            if (winning_digits <= 0) return 9.0;
            return parseFloat((9.0 / winning_digits).toFixed(2));
        }
        case 'DIGITUNDER': {
            // digits below barrier = barrier out of 10
            const winning_digits = Number(barrier);
            if (winning_digits <= 0) return 9.0;
            return parseFloat((9.0 / winning_digits).toFixed(2));
        }

        case 'CALL':
        case 'PUT':
        case 'RISE':
        case 'FALL':
            return 1.95; // ~50/50 ticks contracts

        default:
            return 1.8;
    }
}
